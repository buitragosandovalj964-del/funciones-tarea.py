#Escribe un programa en Python que convierta
#  kilómetros a millas utilizando el factor 0.621371, 
# elige un valor en kilómetros (por ejemplo, 10), y muestre en la consola el valor original y el convertido.
kilometros = 10
factor_conversion = 0.621371
millas = kilometros * factor_conversion
print(f"Valor original: {kilometros} km")
print(f"Valor convertido: {millas} mi")